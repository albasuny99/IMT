#include <stdio.h>
void main (void)
{
	int num,i,x;
	
	
	
	printf("enter your num : ");
	scanf("%d",&num);
	
	for (i=0; i<num; i++)
	{
		for(x=0; x<num-1-i; x++)
		{
			printf(" ");
		}
		
		for (x=0; x<2*i+1;x++)
		{
			printf("*");
		}
		
		printf("\n");
	}
	
	
	
}